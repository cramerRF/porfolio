char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	c %= 256;
	while (s[i] != '\0')
	{
		if ((int) s[i] == c)
			return ((char *) s + i);
		i++;
	}
	if (s[i] == c)
		return ((char *) s + i);
	return (0);
}
